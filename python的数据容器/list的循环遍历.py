def while_list():
    list1 = ["python", "ealsen", "itheima"]
    index = 0
    while index < len(list1):
        yuansu = list1[index]
        print(yuansu)
        index += 1

while_list()

print("------------------------")

def for_list():
    list2 = ["python", "itheima", "ealsen"]
    for yuansu in list2:
        print(yuansu)

for_list()